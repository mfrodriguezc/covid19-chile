    private void ObtenerDatosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ObtenerDatosActionPerformed
        int o = 0;
        Instances Datos = null;
        String N = null, E = null;
        
        if(Opciones.getSelectedItem().equals("Dias-Casos")){
            o=1;
            N="Casos";
            E="Escala dias-casos";
        }else if(Opciones.getSelectedItem().equals("Dias-Muertes")){
            o=2;
            N="Muertes";
            E="Escala dias-muertes";
        }
        
        try {
            Datos = L.ObtenerDS("COVID19.csv", o, "DS");
        } catch (Exception ex) {
            Logger.getLogger(RegresionLineal.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        try {
            Ecuacion.setText((String) L.RegresionLineal(o).get(0));
        } catch (Exception ex) {
            Logger.getLogger(RegresionLineal.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        XYSeries Serie = new XYSeries(E);
        for(int x=0;x<Datos.size();x++){
            Serie.add(Datos.get(x).value(0), Datos.get(x).value(1));
        }
        
        XYSeriesCollection Coleccion = new XYSeriesCollection();
        Coleccion.addSeries(Serie);
        
        JFreeChart Grafico = ChartFactory.createXYLineChart("Dias-"+N, "Dias", N, Coleccion,PlotOrientation.VERTICAL,true, false, false);
        ChartPanel PanelGrafico = new ChartPanel(Grafico);
        Panel.setLayout(new java.awt.BorderLayout());
        Panel.add(PanelGrafico);
        Panel.validate();
    }//GEN-LAST:event_ObtenerDatosActionPerformed

