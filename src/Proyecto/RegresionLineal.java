    private void ObtenerPronosticoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ObtenerPronosticoActionPerformed
        int o=0;
        double Y = 0, M = 0, X = 0, B = 0;
        ArrayList <Object> R = new ArrayList<>();
        
        if(Opciones.getSelectedItem().equals("Dias-Casos")){
            o=1;
        }else if(Opciones.getSelectedItem().equals("Dias-Muertes")){
            o=2;
        }
                
        VariableX2.setText(VariableX1.getText());
        try {
            R = L.RegresionLineal(o);
        } catch (Exception ex) {
            Logger.getLogger(RegresionLineal.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        M = (double) R.get(1);
        X = Double.parseDouble(VariableX1.getText());
        B = (double) R.get(2);
        Y=M*X+B;
        if(Y<0){
        Y=0;
        }
        VariableY.setText(String.valueOf(Y));
    }//GEN-LAST:event_ObtenerPronosticoActionPerformed

